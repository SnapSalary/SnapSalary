import React, {useState, useEffect} from 'react';
import axios from 'axios';
// import {ReactDOM} from 'react-dom';


export type FilterBarProps = {
  onApply: (filterString: string) => void,
}


export function SearchBar({onApply}: FilterBarProps) {
  const [filterString, setFilterString] = useState('');

  const handleChange = (event: { target: { value: React.SetStateAction<string>; }; }) => {
    // onApply(filterString); // this is a bad idea, if you want to do this: use lodash.debounce!
    setFilterString(event.target.value);
  };

  const handleKeyDown = (event: any) => {
    if (event.key === 'Enter') {
      onApply(filterString);
    }
  };

  return (
    <>
      <nav className='flex m-auto items-center '>
        <div className='m-7'>
          <h1 className='font-bold'>Snap Salary</h1>
        </div>
        <div className="flex justify-center bg-gray">
          <div className="mb-3 xl:w-96 mt-5">
            <div className="input-group relative flex items-stretch w-full mb-4">
              <input
                className="form-control relative flex-auto min-w-0 block w-full px-3 py-1.5 text-base font-normal text-gray-700 bg-white bg-clip-padding border border-solid border-gray-300 rounded transition ease-in-out m-0 focus:text-gray-700 focus:bg-white focus:border-blue-600 focus:outline-none"
                placeholder="Search by Company, Tittle, City"
                aria-label="Search"
                aria-describedby="button-addon2"
                id="filter"
                type="text"
                value={filterString}
                onChange={handleChange}
                onKeyDown={handleKeyDown}
              /> &nbsp;
              <button className="btn inline-block px-6 py-2.5 bg-blue-600 text-white font-medium text-xs leading-tight uppercase rounded shadow-md hover:bg-blue-700 hover:shadow-lg focus:bg-blue-700  focus:shadow-lg focus:outline-none focus:ring-0 active:bg-blue-800 active:shadow-lg transition duration-150 ease-in-out flex items-center"
                type="button"
                id="button-addon2"
                onClick={() => onApply(filterString)}>
                <svg aria-hidden="true" focusable="false" data-prefix="fas" data-icon="search" className="w-4" role="img" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512">
                  <path fill="currentColor" d="M505 442.7L405.3 343c-4.5-4.5-10.6-7-17-7H372c27.6-35.3 44-79.7 44-128C416 93.1 322.9 0 208 0S0 93.1 0 208s93.1 208 208 208c48.3 0 92.7-16.4 128-44v16.3c0 6.4 2.5 12.5 7 17l99.7 99.7c9.4 9.4 24.6 9.4 33.9 0l28.3-28.3c9.4-9.4 9.4-24.6.1-34zM208 336c-70.7 0-128-57.2-128-128 0-70.7 57.2-128 128-128 70.7 0 128 57.2 128 128 0 70.7-57.2 128-128 128z"></path>
                </svg>
              </button>
            </div>
          </div>
        </div>
      </nav>
    </>
  );
}


